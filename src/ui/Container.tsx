//////////////////////////////////////////////////////////////////////////////////////////////////////
// IMPORTS
//////////////////////////////////////////////////////////////////////////////////////////////////////
import { ReactNode } from "react";

//////////////////////////////////////////////////////////////////////////////////////////////////////
// TYPE
//////////////////////////////////////////////////////////////////////////////////////////////////////
interface ContainerProps {
    children: ReactNode;
    containerID: string;
}

//////////////////////////////////////////////////////////////////////////////////////////////////////
// STYLE
//////////////////////////////////////////////////////////////////////////////////////////////////////
const containerStyle = "flex flex-col cursor-triangle";

//////////////////////////////////////////////////////////////////////////////////////////////////////
// CONTAINER
//////////////////////////////////////////////////////////////////////////////////////////////////////
export default function Container({children, containerID}: ContainerProps) {
    return (
        <div id={containerID} className={containerStyle}>
            {children}
        </div>
    );
}
